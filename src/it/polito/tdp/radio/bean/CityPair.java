package it.polito.tdp.radio.bean;

import java.util.*;

public class CityPair {
	private int c1;
	private int c2;
	private int ponti;
	public CityPair(int c1, int c2, int ponti) {
		super();
		this.c1 = c1;
		this.c2 = c2;
		this.ponti = ponti;
	}
	public int getC1() {
		return c1;
	}
	public void setC1(int c1) {
		this.c1 = c1;
	}
	public int getC2() {
		return c2;
	}
	public void setC2(int c2) {
		this.c2 = c2;
	}
	public int getPonti() {
		return ponti;
	}
	public void setPonti(int ponti) {
		this.ponti = ponti;
	}
	 
	
	
	
	

}
